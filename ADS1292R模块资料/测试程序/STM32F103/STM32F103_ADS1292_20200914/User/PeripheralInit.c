//-----------------------------------------------------------------
// void PeripheralInit(void)
//-----------------------------------------------------------------
//
// 函数功能: 系统外设初始化
// 入口参数: 无
// 返 回 值: 无
// 注意事项: 无
//
//-----------------------------------------------------------------
void PeripheralInit(void)
{
//  LCD_Init();               			// 液晶初始化
  GPIO_LED_Configuration(); 			// LED初始化
	uart_init(115200);							// 串口初始化
	GPIO_ADS1292_Configuration();		// ADS1292引脚初始化
	SPI1_Init();										// SPI1初始化
  ADS1292_PowerOnInit();					// ADS1292上电初始化
}

